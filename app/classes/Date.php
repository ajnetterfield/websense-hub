  public static function parse($str) {
    $full_date_array = date_parse($str);
    $date = array_slice($full_date_array, 0, 3);
    $time = array_slice($full_date_array, 3, 3);
    $date_string = implode("-", $date) . " " . implode(":", $time);
    return $date_string;
  }

  public static function parse_from_format($str, $format) {
    return date_parse_from_format($format, $str);
  }

